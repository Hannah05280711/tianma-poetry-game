async function seedPoets() {
  const conn = await mysql.createConnection(process.env.DATABASE_URL);
  console.log("✅ 连接数据库成功");

  let inserted = 0;
  let updated = 0;

  for (const poet of poets) {
    const [existing] = await conn.query("SELECT id FROM poets WHERE name = ?", [poet.name]);
    
    if (existing.length > 0) {
      await conn.query(
        `UPDATE poets SET dynasty=?, mbtiType=?, mbtiDescription=?, personalityTags=?, signaturePoems=?, styleKeywords=?, relatedWeapons=?, dynastyWeight=? WHERE name=?`,
        [poet.dynasty, poet.mbtiType, poet.mbtiDescription, poet.personalityTags, poet.signaturePoems, poet.styleKeywords, poet.relatedWeapons, poet.dynastyWeight, poet.name]
      );
      updated++;
    } else {
      await conn.query(
        `INSERT INTO poets (name, dynasty, mbtiType, mbtiDescription, personalityTags, signaturePoems, styleKeywords, relatedWeapons, dynastyWeight) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        [poet.name, poet.dynasty, poet.mbtiType, poet.mbtiDescription, poet.personalityTags, poet.signaturePoems, poet.styleKeywords, poet.relatedWeapons, poet.dynastyWeight]
      );
      inserted++;
    }
  }

  const [countResult] = await conn.query("SELECT COUNT(*) as count FROM poets");
  console.log(`\n📚 诗人数据库统计:`);
  console.log(`   新增: ${inserted} 位`);
  console.log(`   更新: ${updated} 位`);
  console.log(`   总计: ${countResult[0].count} 位诗人`);

  // 按朝代统计
  const [dynastyStats] = await conn.query("SELECT dynasty, COUNT(*) as count FROM poets GROUP BY dynasty ORDER BY count DESC");
  console.log(`\n📊 按朝代分布:`);
  dynastyStats.forEach(r => console.log(`   ${r.dynasty}: ${r.count} 位`));

  await conn.end();
}

seedPoets().catch((e) => {
  console.error("❌ 错误:", e.message);
  process.exit(1);
});
